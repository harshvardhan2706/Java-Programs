package com.practice_dsa.Methods;

public class Swap {
    public static void main(String[] args) {
        int a = 10, b = 20;

        System.out.println(a);
        System.out.println(b);
        

        int temp = a;

        a = b;
        b= temp;

        System.out.println(a);
        System.out.println(b);

    }
}
